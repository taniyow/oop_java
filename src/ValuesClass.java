public class ValuesClass{
    private int base;
    private int loop; 
    private int value;
  
    public void setBaseValue(int num)
    {
        base = num ;
    }
    public int getBaseValue()
    {
        return base;
    }
    
    public void setLoop(int timer)
    {
        loop = timer;
    }
    public int getLoop()
    {
        return loop;
    }
    
    public void setValue(int num)
    {
        value = num;
    }
    public int getValue()
    {
        return value;
    }
}